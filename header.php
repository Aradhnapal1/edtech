<script>
document.addEventListener("DOMContentLoaded", function () {
    const tabs = document.querySelectorAll('.main_tab');
    const currentPage = window.location.pathname.split("/").pop() || "index.php";

    tabs.forEach(tab => {
        if (tab.getAttribute("href") === currentPage) {
            tab.classList.add("active");
        } else {
            tab.classList.remove("active");
        }
    });
});
</script>

<script>
document.addEventListener("DOMContentLoaded", function () {
    const form = document.getElementById("globalCourseSearchForm");
    const input = document.getElementById("globalCourseSearchInput");

    if (!form || !input) return;

    const urlParams = new URLSearchParams(window.location.search);
    const searchFromUrl = urlParams.get("search");
    if (searchFromUrl) {
        input.value = searchFromUrl;
    }

    form.addEventListener("submit", function (event) {
        event.preventDefault();
        const searchText = input.value.trim();
        const targetUrl = searchText
            ? `course.php?search=${encodeURIComponent(searchText)}`
            : "course.php";
        window.location.href = targetUrl;
    });
});
</script>


